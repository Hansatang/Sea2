public class parkingMeter
{
  public int minutes;

  public parkingMeter(int minutes)
  {
    this.minutes = minutes;
  }
  public parkingMeter()
  {
    this.minutes = minutes;
  }

  public int getMinutes()
  {
    return minutes;
  }

  public void setMinutes(int minutes)
  {
    this.minutes = minutes;
  }
}
